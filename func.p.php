<?php

// --------------------------------------------------------------------------
// Сборка параметров QUERY_STRING
// --------------------------------------------------------------------------

function p($arr) {
	$r = array();
	ksort($arr);

	foreach($arr as $k=>$v) {
		if($v) {
			$r[] = $k . '=' . urlencode($v);
		}
	}

	return $r ? '?' . implode('&', $r) : ''; 
}

?>