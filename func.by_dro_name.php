// --------------------------------------------------------------------------
// Сортировка по ord DESC + name ASC
// --------------------------------------------------------------------------

function by_dro_name(&$x, &$y) {
	if($x['ord'] > $y['ord']) {
		return 1;
	} elseif($x['ord'] == $y['ord']) {
		return strcmp($x['name'], $y['name']) > 0 ? 1 : 0;
	}
	return 0;
}

